                            <li>Immediately report concerns or allegations of child abuse in accordance with appropriate
                                procedures of the Society.</li>
                            <li>Ensure I act in ways that respect the confidentiality and privacy of those who may
                                disclose child protection concerns.</li>
                            <li>Report any concerns of child abuse and speak up if I observe concerning behaviour with
